#include <stdio.h>

int main() {
    int عدد_المواد, i;
    float المجموع = 0.0, المعدل, الدرجة;

    printf("أدخل عدد المواد: ");
    scanf("%d", &عدد_المواد);

    for(i = 1; i <= عدد_المواد; ++i) {
        printf("أدخل الدرجة للمادة %d: ", i);
        scanf("%f", &الدرجة);
        المجموع += الدرجة;
    }

    المعدل = المجموع / عدد_المواد;
    printf("المعدل الكلي: %.2f\n", المعدل);

    return 0;
}
